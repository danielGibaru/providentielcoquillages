<h1  id='titre'>Histoire</h1>
<p>« Passionné par l’environnement et persuadé que la nature peut subvenir à tous nos besoins, Providentiel Cosmetics a recherché les meilleurs actifs naturels pour le soin de votre peau. Située en Occitanie, c’est en faisant des recherches sur les produits de la mer, que Providentiel a découvert les bienfaits d’une richesse jusque-là sous-estimée : la nacre.
A la suite de cette découverte, Providentiel Cosmetics s’est alors impliqué dans un travail de recherche autour de ce revêtement afin d’en retirer ses bienfaits et de vous en faire profiter grâce à ses cosmétiques. 
C’est en alliant nature, savoir-faire, tradition, technologie moderne et recherche scientifique que Providentiel Cosmetics a créé sa gamme de cosmétiques. »
</p>
<h1 id='titre'>L’Equipe</h1>
<p>« Rassemblés par l’envie de créer un produit cosmétique biologique en faisant ressortir les bienfaits du monde marin, Providentiel Cosmetics est le fruit de la rencontre entre 3 personnes venant d’horizons culturels et humains différents : Jean Louis Clément, Daniel Moukoko et Gaëtan Leguay.
L’équipe de Providentiel Cosmetics se retrouve dans la complémentarité. D’un côté, grâce à ses nombreuses expériences en entreprise, Jean Louis apporte son savoir-faire. Daniel et Gaëtan de leur côté, apportent tout deux leurs compétences en marketing et en communication ainsi que leur motivation. »
</p>
<h1  id='titre'>Nos engagements</h1>

<p>Respecter l’Homme et la Nature
Associer Innovation et bien être
Garantir une sécurité et une qualité contrôlée
Distribuer des produits Made in France 100% naturels
</p>
<p>Conscient de ses responsabilités, Providentiel Cosmetics s’engage à donner à l’Homme le meilleur de la mer, dans le plus grand respect de la nature et de toutes les personnes participant à l’élaboration de nos produits.
Dans cette optique de production saine, Providentiel Cosmetics s’engage en rédigeant une charte environnementale stricte qui exprime les valeurs de la marque :
Préserver la biodiversité en privilégiant une gestion responsable des ressources et des espaces naturels
Appliquer une démarche environnementale dans le respect des normes et règlementations en vigueur
Optimiser les procédés de production  afin d’utiliser l’eau, les énergies et les matières premières de façon efficiente </p>

